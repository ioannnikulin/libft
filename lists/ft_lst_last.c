#include <stdlib.h>
#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*cur;
	int		lstlen;

	lstlen = ft_lstsize(lst);
	if (!lstlen)
		return (0);
	cur = lst;
	while (-- lstlen)
		cur = cur->next;
	return (cur);
}
