#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	int	lstlen;

	lstlen = ft_lstsize(lst);
	if (!lstlen)
		return ;
	while (lstlen --)
	{
		f(lst->content);
		lst = lst->next;
	}
}
