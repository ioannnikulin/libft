void	*ft_memset(void *s, int c, unsigned long long n)
{
	if (n == 0)
		return (s);
	if (c < 0 || c > 255)
		c = -1;
	while (n --)
	{
		(*(char *)&s[n]) = (char)c;
	}
	return (s);
}
