#include <stddef.h>

char	*ft_strchr(const char *s, int c)
{
	size_t	res;

	res = 0;
	while (s[res] && s[res] != c)
		res ++;
	if (s[res] == c)
		return ((char *)&s[res]);
	return (0);
}
