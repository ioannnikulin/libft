#include <stdlib.h>
#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*cur;
	t_list	*n;

	if (lst == 0)
		return ;
	cur = *lst;
	while (cur)
	{
		n = cur->next;
		del(cur->content);
		free(cur);
		cur = n;
	}
	*lst = 0;
}
