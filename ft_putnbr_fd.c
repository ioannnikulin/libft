#include <unistd.h>
#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	size_t	sz;
	char	*s;

	s = ft_itoa(n);
	if (!s)
		return ;
	sz = ft_strlen(s);
	write(fd, s, sz);
}
